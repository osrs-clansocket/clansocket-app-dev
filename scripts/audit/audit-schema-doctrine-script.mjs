#!/usr/bin/env node
// Schema doctrine runner. Consumes getSchemaCols() once + emits three audits.
// ALL THREE STRICT — exits non-zero on any unexempted violation. No lenient
// mode; exemptions go through explicit EXEMPT_* sets so every tolerated
// deviation is named and justified at the doctrine site, never silently muted.
//
//   1. ID/NAME PAIRING
//      every catalog `<base>_id` paired with `<base>_name` in the same table.
//      Exemptions: EXEMPT_BASES (opaque/session/external ids),
//      EXEMPT_BASE_TABLE_PAIRS (table where <base>_id is PK AND name is bare),
//      EXEMPT_TABLES (catalog source-of-truth tables).
//
//   2. REACHABILITY
//      every schema column AND every schema table must appear as an identifier
//      token in .ts/.tsx/.mjs/.cjs/.js under main/ + shared/ + scripts/.
//      Exemptions: EXEMPT_REACHABILITY_COLUMNS (table.column literals).
//      Currently empty — every orphan is a real fix or a real exemption.
//
//   3. OVERLAP
//      same column name across DIFFERENT table prefixes is forbidden unless
//      named in EXEMPT_OVERLAP_NAMES. STRUCTURAL (W/W/W/W axes) and DEDUP
//      (one-prefix denorm) are auto-passes. AMBIGUOUS is the gated bucket.
//      Currently empty — every ambiguous entry is a rename, dedup, or
//      explicit exemption.
//
// Output:
//   .lint-reports/schema-audit.json — full structured findings
//   stdout                          — per-section summary
// Exit code: 1 if any section has unexempted violations, 0 otherwise.

