// ─── shared helpers ────────────────────────────────────────────────────────

function isWordChar(c) {
    return (
        (c >= "a" && c <= "z") ||
        (c >= "A" && c <= "Z") ||
        (c >= "0" && c <= "9") ||
        c === "_"
    );
}

function harvestTokens(text, into) {
    let buf = "";
    for (let i = 0; i < text.length; i += 1) {
        const c = text[i];
        if (isWordChar(c)) {
            buf += c;
        } else if (buf.length > 0) {
            into.add(buf);
            buf = "";
        }
    }
    if (buf.length > 0) into.add(buf);
}

function walkCodeTokens(dir, into) {
    for (const entry of readdirSync(dir, { withFileTypes: true })) {
        if (SKIP_DIR_NAMES.has(entry.name)) continue;
        const full = join(dir, entry.name);
        if (entry.isDirectory()) {
            walkCodeTokens(full, into);
            continue;
        }
        if (!entry.isFile()) continue;
        const ext = extname(entry.name);
        if (!SCAN_EXTS.has(ext)) continue;
        harvestTokens(readFileSync(full, "utf8"), into);
    }
}

function prefixOf(table) {
    const i = table.indexOf("_");
    return i === -1 ? table : table.slice(0, i);
}

// ─── check 1: ID/NAME PAIRING ──────────────────────────────────────────────

function checkIdNamePairing(cols) {
    const violations = [];
    for (const [table, set] of cols) {
        if (EXEMPT_TABLES.has(table)) continue;
        for (const col of set) {
            if (!col.endsWith(ID_SUFFIX)) continue;
            const base = col.slice(0, -ID_SUFFIX.length);
            if (EXEMPT_BASES.has(base)) continue;
            if (EXEMPT_BASE_TABLE_PAIRS.has(`${base}:${table}`)) continue;
            if (!set.has(`${base}_name`)) {
                violations.push({ table, column: col, missing: `${base}_name` });
            }
        }
    }
    return violations;
}

// ─── check 2: REACHABILITY ─────────────────────────────────────────────────

function checkReachability(cols) {
    const tokens = new Set();
    let scanRootCount = 0;
    for (const r of SCAN_ROOTS) {
        const full = join(ROOT, r);
        if (!existsSync(full)) continue;
        walkCodeTokens(full, tokens);
        scanRootCount += 1;
    }
    const orphansByTable = {};
    const tableTotals = {};
    const tableHits = {};
    const tablesMissing = [];
    let totalCols = 0;
    let totalOrphans = 0;
    for (const [table, set] of cols) {
        tableTotals[table] = set.size;
        let hits = 0;
        const dead = [];
        if (!tokens.has(table) && !EXEMPT_REACHABILITY_TABLES.has(table)) {
            tablesMissing.push(table);
        }
        for (const col of set) {
            totalCols += 1;
            if (tokens.has(col)) {
                hits += 1;
            } else if (EXEMPT_REACHABILITY_COLUMNS.has(`${table}.${col}`)) {
                hits += 1;
            } else {
                dead.push(col);
                totalOrphans += 1;
            }
        }
        tableHits[table] = hits;
        if (dead.length > 0) orphansByTable[table] = dead;
    }
    return {
        scanRootCount,
        uniqueTokens: tokens.size,
        totalCols,
        totalOrphans,
        tablesMissing,
        orphansByTable,
        tableTotals,
        tableHits,
    };
}

// ─── check 3: OVERLAP ──────────────────────────────────────────────────────

function checkOverlap(cols) {
    const byName = new Map();
    for (const [table, set] of cols) {
        for (const col of set) {
            if (!byName.has(col)) byName.set(col, []);
            byName.get(col).push(table);
        }
    }
    const entries = [...byName.entries()].map(([name, tables]) => {
        const prefixes = [...new Set(tables.map(prefixOf))];
        return { name, count: tables.length, prefixes, tables };
    });
    entries.sort((a, b) => b.count - a.count);
    const structural = [];
    const dedup = [];
    const ambiguous = [];
    for (const e of entries) {
        if (e.count < OVERLAP_MIN_COUNT) continue;
        if (STRUCTURAL_NAMES.has(e.name)) structural.push(e);
        else if (EXEMPT_OVERLAP_NAMES.has(e.name)) structural.push({ ...e, exempted: true });
        else if (e.prefixes.length > 1) ambiguous.push(e);
        else dedup.push(e);
    }
    return { uniqueColumns: entries.length, structural, dedup, ambiguous };
}

// ─── reporting ─────────────────────────────────────────────────────────────

function reportIdNamePairing(violations) {
    process.stdout.write("\n[1/3] ID/NAME PAIRING (strict)\n");
    if (violations.length === 0) {
        process.stdout.write("  OK — every catalog <entity>_id paired with <entity>_name.\n");
        return;
    }
    process.stderr.write(`  FAIL — ${violations.length} violation(s):\n`);
    for (const v of violations) {
        process.stderr.write(`    ${v.table}.${v.column} -> missing ${v.missing}\n`);
    }
    process.stderr.write("\n  fix: add the <entity>_name column (denormalized at write time),\n");
    process.stderr.write("       or add the base to EXEMPT_BASES if it is a non-catalog identifier.\n");
}

function reportReachability(r, tableCount) {
    process.stdout.write("\n[2/3] REACHABILITY (strict)\n");
    process.stdout.write(`  scan roots: ${SCAN_ROOTS.join(", ")} (${r.scanRootCount} present, ${r.uniqueTokens} unique tokens)\n`);
    process.stdout.write(`  schema: ${tableCount} tables / ${r.totalCols} columns\n`);
    process.stdout.write(`  ORPHAN columns: ${r.totalOrphans}\n`);
    process.stdout.write(`  ORPHAN tables: ${r.tablesMissing.length}\n`);
    if (r.tablesMissing.length > 0) {
        process.stdout.write("  tables with zero code references:\n");
        for (const t of r.tablesMissing.slice(0, REACH_TABLE_LIMIT)) {
            process.stdout.write(`    - ${t}\n`);
        }
        if (r.tablesMissing.length > REACH_TABLE_LIMIT) {
            process.stdout.write(`    ... and ${r.tablesMissing.length - REACH_TABLE_LIMIT} more\n`);
        }
    }
    const orphanEntries = Object.entries(r.orphansByTable);
    if (orphanEntries.length > 0) {
        orphanEntries.sort((a, b) => b[1].length - a[1].length);
        process.stdout.write("  top tables by orphan count:\n");
        for (const [t, dead] of orphanEntries.slice(0, REACH_TABLE_LIMIT)) {
            const total = r.tableTotals[t];
            const sample = dead.slice(0, REACH_COLUMN_SAMPLE).join(", ");
            const tail = dead.length > REACH_COLUMN_SAMPLE ? "…" : "";
            process.stdout.write(`    - ${t}: ${dead.length}/${total} orphan — ${sample}${tail}\n`);
        }
        if (orphanEntries.length > REACH_TABLE_LIMIT) {
            process.stdout.write(`    ... and ${orphanEntries.length - REACH_TABLE_LIMIT} more\n`);
        }
    }
}

function dumpOverlapBucket(title, list, limit) {
    process.stdout.write(`  ${title} (${list.length}):\n`);
    for (const e of list.slice(0, limit)) {
        process.stdout.write(`    ${e.name} (${e.count}× across ${e.prefixes.length} prefix${e.prefixes.length === 1 ? "" : "es"}: ${e.prefixes.join(",")})\n`);
        const sample = e.tables.slice(0, OVERLAP_SAMPLE_TABLES).join(", ");
        const tail = e.tables.length > OVERLAP_SAMPLE_TABLES ? `, …+${e.tables.length - OVERLAP_SAMPLE_TABLES}` : "";
        process.stdout.write(`      tables: ${sample}${tail}\n`);
    }
    if (list.length > limit) {
        process.stdout.write(`    ... and ${list.length - limit} more\n`);
    }
}

function reportOverlap(o) {
    process.stdout.write("\n[3/3] OVERLAP (strict on AMBIGUOUS bucket)\n");
    process.stdout.write(`  ${o.uniqueColumns} unique column names\n`);
    dumpOverlapBucket("STRUCTURAL (known-shared axes — expected)", o.structural, OVERLAP_BUCKET_LIMIT);
    dumpOverlapBucket("DEDUP (denormalization within one prefix)", o.dedup, OVERLAP_BUCKET_LIMIT);
    dumpOverlapBucket("AMBIGUOUS (cross-prefix overload — review)", o.ambiguous, OVERLAP_BUCKET_LIMIT);
}

// ─── main ──────────────────────────────────────────────────────────────────

const cols = getSchemaCols();
const violations = checkIdNamePairing(cols);
const reach = checkReachability(cols);
const overlap = checkOverlap(cols);

const OUT_DIR = join(ROOT, ".lint-reports");
const OUT_PATH = join(OUT_DIR, "schema-audit.json");
mkdirSync(OUT_DIR, { recursive: true });
writeFileSync(
    OUT_PATH,
    JSON.stringify(
        {
            generated_at: new Date().toISOString(),
            schema_tables: cols.size,
            id_name_pairing: { violations },
            reachability: reach,
            overlap: {
                unique_columns: overlap.uniqueColumns,
                structural: overlap.structural,
                dedup: overlap.dedup,
                ambiguous: overlap.ambiguous,
            },
        },
        null,
        2,
    ) + "\n",
);

process.stdout.write(`schema audit — ${cols.size} tables\n`);
reportIdNamePairing(violations);
reportReachability(reach, cols.size);
reportOverlap(overlap);
process.stdout.write(`\nfull report: ${OUT_PATH}\n`);

const fail =
    violations.length > 0 ||
    reach.totalOrphans > 0 ||
    reach.tablesMissing.length > 0 ||
    overlap.ambiguous.length > 0;

if (fail) {
    process.stderr.write("\nschema doctrine FAIL — fix violations above OR add explicit entries to EXEMPT_BASES / EXEMPT_REACHABILITY_* / EXEMPT_OVERLAP_NAMES with a one-line rationale.\n");
    process.exit(1);
}
process.stdout.write("\nschema doctrine OK — all three sections clean.\n");
