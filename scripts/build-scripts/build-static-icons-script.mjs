import fs from "node:fs";
import path from "node:path";
import url from "node:url";
import sharp from "sharp";

const here = path.dirname(url.fileURLToPath(import.meta.url));
const resourcesDir = path.resolve(here, "..", "..", "public", "resources", "osrs", "icon_clan_ranks");
const pathsOut = path.resolve(here, "..", "..", "main", "dashboard", "src", "state", "static-icons.json");
const colorsOut = path.resolve(here, "..", "..", "main", "dashboard", "src", "state", "icon-colors.json");
const cssOut = path.resolve(here, "..", "..", "main", "dashboard", "src", "styles", "auto-gen", "rank-colors-tokens.css");
const osrsRoot = path.resolve(here, "..", "..", "public", "resources", "osrs");
const osrsRegistryOut = path.resolve(here, "..", "..", "main", "dashboard", "src", "data", "icons", "osrs.json");
const OSRS_PICKER_FOLDERS = ["icon_hiscores", "icon_pets", "icon_player_types"];

const IMAGE_EXTS = new Set([".png", ".jpg", ".jpeg", ".webp", ".gif", ".svg"]);
const SAMPLE_SIZE = 16;
const PALETTE_COLORS = 64;
const ALPHA_MIN = 128;
const LUMA_MIN = 0.12;
const LUMA_MAX = 0.92;

function walk(dir) {
    const out = [];
    for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
        const full = path.join(dir, entry.name);
        if (entry.isDirectory()) out.push(...walk(full));
        else if (entry.isFile() && IMAGE_EXTS.has(path.extname(entry.name).toLowerCase())) {
            out.push(full);
        }
    }
    return out;
}

function lumaOf(r, g, b) {
    return (0.2126 * r + 0.7152 * g + 0.0722 * b) / 255;
}

function hex(r, g, b) {
    const h = (n) => n.toString(16).padStart(2, "0");
    const r2 = h(r), g2 = h(g), b2 = h(b);
    if (r2[0] === r2[1] && g2[0] === g2[1] && b2[0] === b2[1]) {
        return `#${r2[0]}${g2[0]}${b2[0]}`;
    }
    return `#${r2}${g2}${b2}`;
}

async function dominantColor(file) {
    const quantized = await sharp(file)
        .resize(SAMPLE_SIZE, SAMPLE_SIZE, { kernel: "nearest", fit: "inside" })
        .png({ palette: true, colors: PALETTE_COLORS })
        .toBuffer();
    const { data, info } = await sharp(quantized)
        .ensureAlpha()
        .raw()
        .toBuffer({ resolveWithObject: true });
    const pxs = info.width * info.height;
    const counts = new Map();
    for (let i = 0; i < pxs; i += 1) {
        const o = i * 4;
        const r = data[o];
        const g = data[o + 1];
        const b = data[o + 2];
        const a = data[o + 3];
        if (a < ALPHA_MIN) continue;
        const l = lumaOf(r, g, b);
        if (l < LUMA_MIN || l > LUMA_MAX) continue;
        const key = (r << 16) | (g << 8) | b;
        counts.set(key, (counts.get(key) ?? 0) + 1);
    }
    if (counts.size === 0) return null;
    let bestKey = 0;
    let bestN = -1;
    for (const [k, n] of counts) {
        if (n > bestN) { bestN = n; bestKey = k; }
    }
    return hex((bestKey >> 16) & 0xff, (bestKey >> 8) & 0xff, bestKey & 0xff);
}

const files = walk(resourcesDir);
const publicRoot = path.resolve(here, "..", "..", "public");
const paths = files
    .map((f) => "/" + path.relative(publicRoot, f).split(path.sep).join("/"))
    .sort();

const colors = {};
for (const file of files) {
    const filename = path.basename(file);
    try {
        const color = await dominantColor(file);
        if (color !== null) colors[filename] = color;
    } catch (err) {
        console.warn(`color extraction failed for ${filename}: ${err.message}`);
    }
}

const cssLines = ["/* AUTO-GENERATED by scripts/build-scripts/build-static-icons-script.mjs — do not edit */"];
for (const [filename, color] of Object.entries(colors)) {
    const slug = filename.replace(/\.[^.]+$/, "");
    const cssSlug = slug.replaceAll("_", "-");
    cssLines.push(`.rank-color-${cssSlug}{--rank-accent:${color}}`);
}

fs.writeFileSync(pathsOut, JSON.stringify(paths, null, 2) + "\n");
fs.writeFileSync(colorsOut, JSON.stringify(colors, null, 2) + "\n");
fs.writeFileSync(cssOut, cssLines.join("\n") + "\n");
console.log(`Static icons synced: ${paths.length} path(s) -> ${path.relative(process.cwd(), pathsOut)}`);
console.log(`Icon colors synced: ${Object.keys(colors).length} color(s) -> ${path.relative(process.cwd(), colorsOut)}`);
console.log(`Icon color CSS synced: ${Object.keys(colors).length} rule(s) -> ${path.relative(process.cwd(), cssOut)}`);

const osrsRegistry = [];
for (const folderName of OSRS_PICKER_FOLDERS) {
    const folderDir = path.resolve(osrsRoot, folderName);
    if (!fs.existsSync(folderDir)) continue;
    const folderKey = folderName.replace(/^icon_/, "");
    const entries = fs.readdirSync(folderDir, { withFileTypes: true });
    for (const entry of entries) {
        if (!entry.isFile()) continue;
        const ext = path.extname(entry.name).toLowerCase();
        if (!IMAGE_EXTS.has(ext)) continue;
        const base = entry.name.slice(0, entry.name.length - ext.length);
        osrsRegistry.push(`${folderKey}_${base}`);
    }
}
osrsRegistry.sort();
fs.mkdirSync(path.dirname(osrsRegistryOut), { recursive: true });
fs.writeFileSync(osrsRegistryOut, JSON.stringify(osrsRegistry) + "\n");
console.log(`OSRS picker icons synced: ${osrsRegistry.length} icon(s) -> ${path.relative(process.cwd(), osrsRegistryOut)}`);

process.exit(0);
