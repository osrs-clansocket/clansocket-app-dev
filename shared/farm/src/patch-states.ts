export function forVarbitValue(impl: PatchImplementation, value: number): PatchStateResult | null {
    const ranges = TABLES[impl];
    for (const [lo, hi, produce, state] of ranges) {
        if (value >= lo && value <= hi) {
            return { produce, state };
        }
    }
    return null;
}
